/** Lookup a single product by categorySlug + productSlug */
export const findProduct = (categorySlug, productSlug) =>
  allProducts.find((p) => p.categorySlug === categorySlug && p.slug === productSlug) ?? null

/** Get all products in a category */
export const getProductsByCategory = (categorySlug) =>
  allProducts.filter((p) => p.categorySlug === categorySlug)

/** Get related products: others in same category, up to 4 */
export const getRelatedProducts = (categorySlug, currentSlug) =>
  allProducts.filter((p) => p.categorySlug === categorySlug && p.slug !== currentSlug).slice(0, 4)
